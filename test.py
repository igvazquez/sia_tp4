import numpy as np

A = np.array([[1, 1, 1],
              [2, 200, 2],
              [5, 5, 5]])

B = np.array([[[0, 1, 0],
               [1, 1, 0],
               [1, 1, 1]],
                  [[0, 2, 0],
                   [2, 2, 0],
                   [2, 2, 2]],
                      [[0, 3, 0],
                       [3, 3, 0],
                       [3, 3, 3]]])

print(np.einsum('ij, ijk->ijk', A, B))
